# 변경된 사진의 이름은 f"{원본사진 이름}_resize.{원본사진의 확장자}" 형태로 저장하세요.
# 예를 들어, 원본 사진 이름이 cat.jpg라면, 변경된 사진의 이름은 cat_resize.jpg가 되어야 합니다.
